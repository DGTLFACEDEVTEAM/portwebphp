<p>6698 sayılı Kişisel Verilerin Korunması Kanunu'na (&ldquo;Kanun&rdquo;) göre <strong>ARAS İNŞAAT TİCARET TURİZM ANONİM ŞİRKETİ</strong> (&ldquo;Bundan sonra &lsquo;<strong>PORT NATURE&rsquo;</strong> olarak anılacaktır.&rdquo;) sizinle ilgili kişisel verileri işlemesi sebebiyle veri sorumlusu olarak tanımlanmaktadır. Kanunun &quot;Aydınlatma Yükümlülüğü&quot; başlıklı 10'ncu maddesine göre veri sorumluları, kişisel verilerini işledikleri gerçek kişileri bazı konularda bilgilendirmekle yükümlüdür.</p>

<p>Bu metin hem mevzuata uyum sağlanması hem de şeffaf ve hesap verebilir olabilmek amacıyla <strong>PORT NATURE </strong>tarafından hazırlanmıştır.</p>

<p><strong>İlgili Kişi: </strong>Şirketimize iş başvurusunda bulunmanız nedeniyle çalışan adayı sıfatına sahip olmanız ve bu nedenle birtakım kişisel verilerini işlediğimiz siz; kanun tarafından ilgili kişi olarak tanımlanmaktasınız.</p>

<p><strong>Veri Sorumlusu:</strong> Sizinle ilgili kişisel veriler konusunda kişisel verilerin işleme amaçlarını ve vasıtalarını belirleyen, veri kayıt sisteminin kurulmasından ve yönetilmesinden sorumlu olan <strong>[PORT NATURE]<em> </em></strong>veri sorumlusudur.</p>

<p><strong>Kişisel veri:</strong> Kimliği belirli veya belirlenebilir gerçek kişiye ilişkin her türlü bilgiyi ifade eder.</p>

<p><strong>&Ouml;zel Nitelikli Kişisel Veri:</strong> Kişilerin ırkı, etnik kökeni, siyasi düşüncesi, felsefi inancı, dini, mezhebi veya diğer inançları, kılık ve kıyafeti, dernek, vakıf ya da sendika üyeliği, sağlığı, cinsel hayatı, ceza mahkumiyeti ve güvenlik tedbirleriyle ilgili verileri ile biyometrik ve genetik verileri ifade eder.</p>

<p>6698 Sayılı Kişisel Verilerin Korunması Kanunu&rsquo;na göre kişisel verileriniz açık rızanız dahilinde ya da kanunda belirtilen istisnai hallerde açık rızanız olmadan işlenebilir.<br />
<br />
Bu bilgilendirme metninin devamında;</p>

<p>Kişisel verilerinizin hangi amaçlarla hangi KVKK gerekçesi (Açık rızanız dahilinde mi yoksa kanundaki diğer istisnalar dahilinde mi işlendiği) ile işlendiği,</p>

<p>Kişisel verilerinizin hangi kişi ve kurumlara aktarılabileceği,</p>

<p>Kişisel verileriniz ile ilgili haklarınız,</p>

<p>Belirttiğimiz haklarınızı hangi iletişim adresini kullanarak talep edebileceğiniz,</p>

<p>Talebi bize nasıl ileteceğiniz konularında bilgilendirileceksiniz.</p>

<p><strong>Süreçlere Göre İşlenen Kişisel Verileriniz, İşlenme Amaçları ve Hukuki Sebepleri</strong></p>

<p>İş başvuru formunda doldurmuş olduğunuz;</p>

<p><li> Kimlik (Ad, Soyad, Doğum Yeri, Doğum Tarihi, Cinsiyet), İletişim (Cep Telefonu, Ev Telefonu, Adres), Hukuki İşlem (Başvuru Tarihi), Mesleki Deneyim (&Ouml;ğrenim Bilgileri, Yabancı Dil Bilgileri vd.), Çalışan Adayı İşlem (&Ouml;nceki işveren bilgileri), Görsel ve İşitsel Kayıtlar (Fotoğraf) verileriniz</p>

<p>o Çalışan Adaylarının Başvuru Süreçlerinin Yürütülmesi (İş Başvurunuzun Değerlendirmeye Alınması) amacıyla</p>

<p><li> İş başvuru formunda beyan ettiğiniz açık rızanıza istinaden elde edilmektedir.</p>

<p>Belirttiğimiz kişisel verileriniz; Kanun m.5/2(f): ilgili kişinin temel hak ve özgürlüklerine zarar vermemek kaydıyla, veri sorumlusunun meşru menfaatleri için veri işlenmesinin zorunlu olması bendine istinaden, saklama ve arşiv faaliyetlerinin yürütülmesi amacıyla saklanmaktadır.</p>

<p>Erkek çalışansanız askerlik durum bilgileriniz,</p>

<p>Görsel bir iş pozisyonuna başvuruyorsanız fotoğraf verileriniz,</p>

<p>Araç ve cihaz kullanımı gerektiren pozisyona başvuruyorsanız işle ilgili kullanabileceğiniz araç ve cihaz bilgileriniz,</p>

<p>Ehliyet gerektiren bir iş pozisyonuna başvuruyorsanız ehliyet bilgileriniz yukarıda belirttiğimiz verilere ek olarak yine yukarıda belirtilen kişisel veri işleme amaçları ve kanun gerekçeleri dahilinde elde edilmekte ve saklanmaktadır.</p>

<p><strong>Kişisel Verilerin Kimlere ve Hangi Amaçla Aktarılabileceği</strong></p>

<p>Kişisel verileriniz KVKK Md.28/1 çerçevesinde talep edilmesi halinde aydınlatma yükümlülüğü olmadan ve açık rızanız aranmadan ilgili makamlara aktarılabilir.</p>

<p>Bunun dışında öngörülemeyen durumlarda da kişisel verileriniz kanunlarda açıkça belirtilmiş durumlarda talep edilmesi halinde kanunlarda belirtilen kamu kurumlarına (Bakanlıklar gibi idari makamlara) kanunda öngörülen amaç ve sınırlamalar dahilinde aktarılabilir.</p>

<p>Aydınlatma yükümlülüğü olmayan ve açık rızanızı gerektirmeyen yasal yükümlülüklerimizin dışında; kişisel verilerinizin aktarımı yapılmamaktadır.</p>

