<p><strong>Kişisel Verilerin Kimlere ve Hangi Amaçla Aktarılabileceği</strong></p>

<p>Kişisel verileriniz KVKK Md.28/1 çerçevesinde talep edilmesi halinde aydınlatma yükümlülüğü olmadan ve açık rızanız aranmadan ilgili makamlara aktarılabilir.</p>

<p>Bunun dışında öngörülemeyen durumlarda da kişisel verileriniz kanunlarda açıkça belirtilmiş durumlarda talep edilmesi halinde kanunlarda belirtilen kamu kurumlarına (Bakanlıklar gibi idari makamlara) kanunda öngörülen amaç ve sınırlamalar dahilinde aktarılabilir.</p>

<p>Aydınlatma yükümlülüğü olmayan ve açık rızanızı gerektirmeyen yasal yükümlülüklerimizin dışında;</p>

<p>Reklam / Kampanya / Promosyon Süreçlerinin Yürütülmesi<em> </em>amacıyla<em> </em>kanun m.9'da belirtilen şartlar çerçevesinde veri merkezi yurtdışında olan sosyal medya platformlarına aktarılacak, sosyal medya aracılığıyla alenileştirilecek, kanun m.8'de öngörülen şartlar çerçevesinde web sitemiz ve veri merkezi yurtiçinde bulunan diğer platformlarda alenileştirilecektir.</p>

