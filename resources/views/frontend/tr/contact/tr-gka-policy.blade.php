<p>6698 sayılı Kişisel Verilerin Korunması Kanunu&#39;na (&ldquo;Kanun&rdquo;) göre <strong>ARAS İNŞAAT TİCARET TURİZM ANONİM ŞİRKETİ</strong> (&ldquo;Bundan sonra &lsquo;<strong>PORT NATURE&rsquo;</strong> olarak anılacaktır.&rdquo;) sizinle ilgili kişisel verileri işlemesi sebebiyle veri sorumlusu olarak tanımlanmaktadır. Kanunun &quot;Aydınlatma Yükümlülüğü&quot; başlıklı 10&#39;ncu maddesine göre veri sorumluları, kişisel verilerini işledikleri gerçek kişileri bazı konularda bilgilendirmekle yükümlüdür.</p>

